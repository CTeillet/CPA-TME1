package utils;

public class Triangle {
    Long a;
    Long b;
    Long c;

    public Triangle(Long a, Long b, Long c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public Long getA() {
        return a;
    }

    public Long getB() {
        return b;
    }

    public Long getC() {
        return c;
    }
}
